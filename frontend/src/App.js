import React, { useState, useEffect, useCallback } from 'react';
import DataProfiling from './components/DataProfiling';
import DataQualityRules from './components/DataQualityRules';
import RootCauseAnalysis from './components/RootCauseAnalysis';
import './styles/App.css';

const API_BASE = 'http://localhost:8000';

function App() {
  const [activeTab, setActiveTab] = useState('profiling');
  const [health, setHealth] = useState(null);
  const [error, setError] = useState(null);

  useEffect(() => {
    fetch(`${API_BASE}/health`)
      .then(res => res.json())
      .then(data => setHealth(data))
      .catch(err => setError('API not available'));
  }, []);

  const tabs = [
    { id: 'profiling', label: 'Data Profiling', icon: 'ğŸ“Š' },
    { id: 'rules', label: 'Quality Rules', icon: 'âœ“' },
    { id: 'analysis', label: 'Root Cause Analysis', icon: 'ğŸ”' },
  ];

  return (
    <div className="app">
      <header className="app-header">
        <h1>Data Quality Management</h1>
        <span className="version">Expert AI v1.0</span>
        {health && <span className="status healthy">â— API Connected</span>}
        {error && <span className="status error">â— {error}</span>}
      </header>

      <nav className="tabs">
        {tabs.map(tab => (
          <button
            key={tab.id}
            className={`tab ${activeTab === tab.id ? 'active' : ''}`}
            onClick={() => setActiveTab(tab.id)}
          >
            <span className="icon">{tab.icon}</span>
            {tab.label}
          </button>
        ))}
      </nav>

      <main className="content">
        {activeTab === 'profiling' && <DataProfiling apiBase={API_BASE} />}
        {activeTab === 'rules' && <DataQualityRules apiBase={API_BASE} />}
        {activeTab === 'analysis' && <RootCauseAnalysis apiBase={API_BASE} />}
      </main>

      <footer className="app-footer">
        <p>Generated by Expert AI | Using LOCAL AI for analysis (RTX 5090 + RTX 3050)</p>
      </footer>
    </div>
  );
}

export default App;
